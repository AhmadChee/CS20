


public class Circle 
{

	private double radius;
	
	public Circle()//constructor method
	{
		radius = 1; //default radius
				
	   
	}
	
	public Circle(double r)//constructor method2
	{
		radius = r;
		
		
	}
	
	public void setRadius(double newRadius)
	{
		radius = newRadius;
	}
	
	
	public double getRadius()

	{
		return(radius);
	}
	
	
	

}
